package com.dataTypes;

public class PrimitiveDataType {
	
	public static void main(String[] args) {
		
		// Data type   Variable  = Value;
		
		boolean b = false;  // the boolean data type is used to represent a value that can either be true or false
		byte e = -123;  //byte data type is a primitive data type that represents an 8-bit signed two's complement integer. It has a minimum value of -128 and a maximum value of 127 (inclusive).
		short f = 3213;
		int i = 213454;
		long l = 1234567256;
		float g = 32.34f;
		double d = 789.99d;
		char k = 'k';
		
		System.out.println(b);
		System.out.println(f);
		System.out.println(e);
		System.out.println(i);
		System.out.println(l);
		System.out.println(g);
		System.out.println(d);
		System.out.println(k);
	}

}
